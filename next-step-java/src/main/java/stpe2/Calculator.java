public class Calculator {
    public int add(int i, int j){
        return i+j;
    }

    public int substract(int i, int j){
        return i-j;
    }

    public int multiply(int i, int j){
        return i*j;
    }

    public int divide(int i, int j){
        return i/j;
    }
}
